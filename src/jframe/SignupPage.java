     public boolean validateSignup(){
        String name = txt_username.getText();
        String pwd =String.valueOf(pass_field.getPassword());
        String email = txt_email.getText();
        String contact = txt_contact.getText();
        
        if (name.equals("")) {
            JOptionPane.showMessageDialog(this, "Veuiller Taper Votre nom d'utlisateur");
            return false;
        }
        
        if (pwd.equals("")) {
            JOptionPane.showMessageDialog(this, "Veuiller Taper Votre mot de passe");
            return false;
        }
        
        if (email.equals("") || !email.matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Veuiller Taper Votre Email valide");
            return false;
        }
        
         if (contact.equals("")) {
            JOptionPane.showMessageDialog(this, "Veuiller Taper Votre contact");
            return false;
        }
        
        return true;
    }
